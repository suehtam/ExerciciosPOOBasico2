/*
3. Números de 1 a 50 com for
Desenvolva um programa que imprima os números de 1 a 50, um por linha, usando um
loop for tradicional.
*/
class Exercicio3
{
    public static void main(String[] args) 
    {
        for(int i = 1; i <= 50; i++)
        {
            System.out.println(i);
        }
    }
}
